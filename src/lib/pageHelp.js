// pathname 으로 매칭. 동적 라우트(예: /approvals/abc-123)는 점진적 축소
export function getPageHelp(pathname) {
  if (!pathname) return null;
  if (PAGE_HELP[pathname]) return PAGE_HELP[pathname];

  // /approvals/[id]/print → /approvals 까지 줄여가며 매칭
  const segs = pathname.split('/').filter(Boolean);
  while (segs.length > 0) {
    const p = '/' + segs.join('/');
    if (PAGE_HELP[p]) return PAGE_HELP[p];
    segs.pop();
  }
  return null;
}
