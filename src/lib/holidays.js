export function isHoliday(date) {
  const key = `${date.getFullYear()}-${String(date.getMonth() + 1).padStart(2, '0')}-${String(date.getDate()).padStart(2, '0')}`;
  const y = date.getFullYear();
  return HOLIDAYS_BY_YEAR[y]?.[key] ?? null;
}
